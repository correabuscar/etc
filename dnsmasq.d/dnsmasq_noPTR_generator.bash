function print_all_hostnames_from_etc_hosts() {
  cat /etc/hosts /etc/hosts.dnsmasq | grep -v '^\s*#'|grep -v '^\s*$'|sed -re 's/^[0-9.: \t]+//'|sed -re 's/[[:space:]]+/ /g'|tr ' ' '\n'|sort -u
}

